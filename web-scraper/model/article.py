class Article:

    def __init__(self, title, article_type, text, keywords, datetime, source, url, number_of_views):
        self.title = title
        self.article_type = article_type
        self.text = text
        self.keywords = keywords
        self.datetime = datetime
        self.source = source
        self.url = url
        self.number_of_views = number_of_views